/**
ให้คํานวณเงินฝากสะสมโดยปีที่ 1 มีเงินเริ่มต้น 100,000 บาท
- ธนาคารให้ดอกเบี้ยร้อยละ 2.5 ต่อปีโดยจขายดอกเบี้ยปีละครั้ง
- ดอกเบี้ยที่ได้จะสะสมและทบยอดไปในเงินฝากปีถัดไป
- เมื่อครบ 10 ปีจะมียอดเงินฝากสะสมเท่าใด (ไม่มีการฝากเพิ่มและถอนออก) 
 */
let money = 100000;
let interest = 2.5;
for(let i = 1; i <= 10; i++){
    money = money +(money*interest/100);
    console.log("End of the year " + i + " Your money is " + money);
}